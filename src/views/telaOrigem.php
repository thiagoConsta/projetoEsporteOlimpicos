    <h2>1963: O PRIMEIRO DE MUITOS CAMPEONATOS DE SKATE</h2>
    <p>
        Em 1963, aconteceu a primeira competição oficial de skate da história, realizada na cidade litorânea
        de Hermosa Beach, Califórnia. O grande vencedor foi o americano Larry Stevenson, um nome que viria a
        ser fundamental para o crescimento do esporte.

        Richard Lawrence “Larry” Stevenson tinha a competitividade no sangue. Ele foi o responsável por
        criar o kicktail, aquela curvatura para cima na extremidade do skate que possibilitou a realização
        da maioria das manobras modernas.

        Junto com sua esposa, Stevenson fundou a Makaha, uma das primeiras marcas de skate do mundo. No
        começo, os skates eram produzidos artesanalmente na garagem de sua casa, mas a marca rapidamente
        ganhou força, formando um time de peso com nomes como Bruce Logan, Woody Woodward, Danny Bearer,
        Scott Archer, Gregg Carroll, John Fries, Joey Saenz, George Trafton Jr. e Squeek Blank.

        Mesmo com algumas evoluções, o skate ainda estava fortemente ligado ao surf, e muitos praticantes
        alternavam entre as duas modalidades, trazendo para o asfalto toda a fluidez e o estilo das ondas.
